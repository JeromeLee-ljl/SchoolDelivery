public class DataTokens {
    private String uid;
    private String token;

    public void setUid(String uid) {
        this.uid = uid;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getUid() {

        return uid;
    }

    public String getToken() {
        return token;
    }
}
